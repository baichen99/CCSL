<template>
  <div class="learning-platform">
    <word-search
      start="A"
      end="L"
      @word-selected="searchByWord"
    />

    <div class="search">
      <h3>国家通用手语学习平台</h3>

      <video-search-input
        v-model="params"
        :show-advance="false"
        @search-clicked="searchByButton"
      />

      <video-search-result
        :videos="videos"
        :limit="params.limit"
        :page="params.page"
        :total="total"
        :sm="24"
        :md="12"
        :lg="12"
        :show-region="false"
        @change-page="changePage"
      />

    </div>

    <word-search
      start="M"
      end="Z"
      @word-selected="searchByWord"
    />
  </div>
</template>

<script>
import VideoSearchInput from "@/components/video/VideoSearchInput.vue";
import VideoSearchResult from "@/components/video/VideoSearchResult.vue";
import WordSearch from "@/components/form/WordSearch.vue";
import { getLexicalVideos } from "@/api/videos";

export default {
  name: "LearningPlatform",
  components: {
    VideoSearchResult,
    VideoSearchInput,
    WordSearch
  },
  data() {
    return {
      videos: [],
      total: 0,
      params: {
        word: "",
        gender: "男",
        region: "100000",
        chinese: "",
        page: 1,
        limit: 4
      }
    };
  },
  methods: {
    clearParams() {
      this.params.word = "";
      this.params.chinese = "";
    },
    getData() {
      let params = this.params;
      getLexicalVideos(params).then(res => {
        this.videos = res.data;
        this.params.page = res.page;
        this.params.limit = res.limit;
        this.total = res.total;
        if (this.total === 0) {
          this.$message("没有找到相关的数据哦～");
        }
      });
    },
    changePage(page) {
      this.params.page = page;
      this.getData();
    },
    searchByButton() {
      this.params.word = "";
      this.params.page = 1;
      this.getData();
    },
    searchByWord(word) {
      this.clearParams();
      this.params.word = word;
      this.params.page = 1;
      this.getData();
    }
  }
};
</script>

