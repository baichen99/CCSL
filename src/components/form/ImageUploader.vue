<template>
  <el-upload
    ref="uploader"
    class="upload-container"
    drag
    :multiple="false"
    :accept="acceptType"
    :limit="1"
    :headers="{
      Authorization: `Bearer ${$store.getters.token}`
    }"
    :action="'/api/files?dir='+dir"
    :on-success="onSuccess"
    :on-error="onError"
    :on-remove="clearFile"
    :before-upload="beforeUpload"
    :show-file-list="false"
    :with-credentials="true"
  >
    <div v-if="fileUrl === ''" class="el-upload__text">
      <i class="el-icon-upload" />
      <div class="upload-text">
        {{ $t("drag") }}
        <em>{{ $t("click") }}</em>
        <br />
        {{ $t("size") }}
      </div>
    </div>
    <img v-else :src="settings.publicURL + fileUrl" alt="image" />
  </el-upload>
</template>

<i18n>
{
  "zh-CN": {
    "drag": "将文件拖到此处，或",
    "click": "点击上传",
    "size": "大小不超过2Mb",
    "sizeError": "文件太大，请压缩后再上传"
  },
  "en-US": {
    "drag": "Drag file to here, or",
    "click": "click to upload",
    "size": "Maximal file size 2Mb",
    "sizeError": "File size exceed limitation, please compress it"
  }
}
</i18n>

<script>
import { mapGetters } from "vuex";
export default {
  name: "ImageUploader",
  model: {
    prop: "url",
    event: "update"
  },
  props: {
    url: {
      type: String,
      default: () => ""
    },
    dir: {
      type: String,
      required: true
    },
    type: {
      type: String,
      default: "all",
      validator: function(value) {
        return ["all", "svg"].indexOf(value) !== -1;
      }
    },
    size: {
      type: Number,
      default: 2
    }
  },
  computed: {
    ...mapGetters(["settings"]),
    fileUrl: {
      get() {
        return this.url;
      },
      set(val) {
        this.$emit("update", val);
      }
    },
    acceptType() {
      if (this.type === "svg") {
        return "image/svg+xml";
      } else {
        return "image/*";
      }
    }
  },
  beforeDestroy() {
    this.clearFile();
  },
  methods: {
    onSuccess(res) {
      const fileUrl = res.data;
      this.fileUrl = fileUrl;
      this.$refs.uploader.clearFiles();
    },
    onError(err) {
      const error = JSON.parse(err.message);
      this.$notify.error({
        title: error.message,
        message: error.error
      });
    },
    beforeUpload(file) {
      const lessThanMaxSize = file.size / 1024 / 1024 < this.size;
      if (!lessThanMaxSize) {
        this.$notify.error({
          title: this.$t("sizeError")
        });
      }
      return lessThanMaxSize;
    },
    clearFile() {
      this.fileUrl = "";
      this.$refs.uploader.clearFiles();
    }
  }
};
</script>

