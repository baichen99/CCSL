<template>
  <el-select
    v-model="data"
    filterable
    clearable
    remote
    :placeholder="$t('tip')"
    :remote-method="filterRemote"
    :loading="loading"
    @clear="filterRemote"
  >
    <el-option v-for="item in options" :key="item.id" :label="item.chinese" :value="item.id">
      {{ item.initial }} -
      <span
        class="tag-value"
        v-html="$options.filters.addNumberSup(item.chinese)"
      ></span>
    </el-option>
  </el-select>
</template>

<i18n>
{
  "zh-CN": {
    "tip": "请输入中文转写"
  },
  "en-US": {
    "tip": "Input Chinese"
  }
}
</i18n>

<script>
import { GetLexicalWordsList } from "@/api/words";
export default {
  name: "WordSelector",
  model: {
    prop: "value",
    event: "update"
  },
  props: {
    value: {
      type: String,
      default: () => ""
    }
  },
  data() {
    return {
      loading: false,
      options: []
    };
  },
  computed: {
    data: {
      get() {
        return this.value;
      },
      set(val) {
        this.$emit("update", val);
      }
    }
  },
  created() {
    this.filterRemote();
  },
  methods: {
    filterRemote(query) {
      this.loading = true;
      GetLexicalWordsList({
        limit: 0,
        chinese: query,
        orderBy: "initial, id"
      })
        .then(res => {
          this.options = res.data;
          this.loading = false;
        })
        .catch(() => {
          this.loading = false;
        });
    }
  }
};
</script>